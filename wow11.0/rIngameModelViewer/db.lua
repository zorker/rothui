-- LoadGlobDefaults
local function LoadGlobDefaults()
  return {
    ["COLOR"] = {1, 1, 1},
    ["MODELSIZE"] = 200,
    ["PAGE"] = 1,
    ["MODEL_LIST"] = cached_model_list,
    ["LAST_DISPLAY_ID"] = 1,
    ["DB_VERSION"] = L.dbversion
  }
end

-- LoadCharDefaults
local function LoadCharDefaults()
  return {}
end

-- load LoadDatabase
local function LoadDatabase()
  -- set saved variables
  rIMV_DBGLOB = rIMV_DBGLOB or LoadGlobDefaults()
  rIMV_DBCHAR = rIMV_DBCHAR or LoadCharDefaults()
  if not rIMV_DBGLOB["DB_VERSION"] or rIMV_DBGLOB["DB_VERSION"] < L.dbversion then
    rIMV_DBGLOB = LoadGlobDefaults()
    rIMV_DBCHAR = LoadCharDefaults()
    print(L.name, 'loading new defaults for db')
  end
  L.DB.GLOB = rIMV_DBGLOB
  L.DB.CHAR = rIMV_DBCHAR
  L.F:SetDisplayIndexList()
end

rLib:RegisterCallback("VARIABLES_LOADED", LoadDatabase)

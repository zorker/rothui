-- SetDBDefaults
local function SetDBDefaults()
  return {
    ["COLOR"] = {1, 1, 1},
    ["MODELSIZE"] = 200,
    ["PAGE"] = 1,
    ["MODEL_LIST"] = cachedModels,
    ["LAST_DISPLAY_ID"] = 301737,
    ["DB_VERSION"] = L.dbversion
  }
end

-- load LoadDatabase
local function LoadDatabase()
  -- set saved variables
  rModelDB_G = rModelDB_G or SetDBDefaults()
  if not rModelDB_G["DB_VERSION"] or rModelDB_G["DB_VERSION"] < L.dbversion then
    rModelDB_G = SetDBDefaults()
    print(L.name, 'loading new defaults for db')
  end
  L.DB.G = rModelDB_G
  print(L.name, 'loading db version', L.DB.G["DB_VERSION"])
end
L.F.LoadDatabase = LoadDatabase
